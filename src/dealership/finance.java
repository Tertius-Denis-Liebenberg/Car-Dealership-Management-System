    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        String gender = "";
        String license = "";
        String vehicle = "";

        if (cmbGender.getSelectedIndex() == 0) {
            gender.equals("Male");
        } else {
            gender.equals("Female");
        }

        if (cmbVehicle.getSelectedIndex() == 0) {
            vehicle.equals("Polo Vivo");

        } else if (cmbVehicle.getSelectedIndex() == 1) {
            vehicle.equals("Polo");

        } else if (cmbVehicle.getSelectedIndex() == 2) {
            vehicle.equals("Polo T-Cros");

        } else {
            vehicle.equals("Polo T-Roc");
        }

        if (rdbYes.isSelected()) {

            //saving to file
            try {
                PrintWriter myWriter = new PrintWriter(txtName.getText() + " " + txtSurname.getText() + ".txt");
                myWriter.println("Personal Information.");
                myWriter.println("---------------------------------------------------------------------");
                myWriter.println("Fullnames: " + txtName.getText() + " " + txtSurname.getText());
                myWriter.println("ID Number: " + txtId.getText());
                myWriter.println("Cell Number: " + txtCell.getText());
                myWriter.println("Gender: " + gender);
                myWriter.println("Ocuupation: " + txtOccupation.getText());
                myWriter.println("Home Address: " + txtHome.getText());
                myWriter.println("Work Address: " + txtWork.getText());
                myWriter.println("Gross Pay: " + txtGross.getText());
                myWriter.println("Net Pay: " + txtNet.getText());
                myWriter.println("Valid Licese: " + license);
                myWriter.println("Model: " + vehicle);

                myWriter.println("\nNext of kin Information.");
                myWriter.println("---------------------------------------------------------------------");
                myWriter.println("Fullnames: " + txtnetName.getText() + " " + txtnextSrname.getText());
                myWriter.println("ID Number: " + txtnextCell.getText());
                myWriter.println("ID Number: " + txtRelationship.getText());
                myWriter.close();
                JOptionPane.showMessageDialog(new finance(), "Application sent.");

            } catch (IOException e) {
                System.out.println("An error occurred.");
                e.printStackTrace();
            }
            new welcomePage().setVisible(true);
            this.setVisible(false);

            license.equals("Yes");
        } else {
            JOptionPane.showMessageDialog(new finance(), "You need a valid license to purchase a car.");
            new welcomePage().setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

