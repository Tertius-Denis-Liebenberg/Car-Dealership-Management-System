    private void btnIDbrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIDbrowseActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            txtattID.setText(selectedFile.getAbsolutePath().toString());
        }
    }//GEN-LAST:event_btnIDbrowseActionPerformed

